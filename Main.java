class Main {
	public float transInGr (float weight){
		float weight_gr = weight*1000;
		return weight_gr;
	}
	public float transInMl(float weight){
		float weight_ml = weight*1000000;
		return weight_ml;
	}
	public float transInT (float weight){
		float weight_t = weight/1000;
		return weight_t;
	}
}
